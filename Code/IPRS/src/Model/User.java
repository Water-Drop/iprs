package Model;

public class User {
	private int id;
	private String Username;
	private String Passeord;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	public String getPasseord() {
		return Passeord;
	}
	public void setPasseord(String passeord) {
		Passeord = passeord;
	}
}
